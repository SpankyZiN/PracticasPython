#Sintaxis -> cadena.capitalize() ->"La palabra 'cadena' es el texto que se utiliza con el metodo"
# Ejemplo 1: Cadena básica
texto = "hola mundo"
resultado = texto.capitalize()
print(resultado)  # Salida: "Hola mundo"

# Ejemplo 2: Cadena con mayúsculas y minúsculas mezcladas
texto = "PyTHon eS gEniAl"
resultado = texto.capitalize()
print(resultado)  # Salida: "Python es genial"

# Ejemplo 3: Cadena que comienza con un número
texto = "123hola mundo"
resultado = texto.capitalize()
print(resultado)  # Salida: "123hola mundo"

