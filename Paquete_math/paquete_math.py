import math
print("=====================================================")
print("a) Constantes")
print("El valor de pi es: ", math.pi) # Muestra el valor de PI
print("El valor de e es: ", math.e)   # Muestra el valor Euler
print("=====================================================")
print("b) Raices y potencias")
print("Raíz cuadrada de 16:", math.sqrt(16))  # Muestra 4.0
print("2 elevado a la 3:", math.pow(2, 3))   # Muestra 8.0
print("=====================================================")
print("c) Logaritmos")
print("Logaritmo natural de 10:", math.log(10))  # Muestra 2.302585...
print("Logaritmo base 10 de 100:", math.log10(100))  # Muestra 2.0
print("=====================================================")
print("d) Trigonometria")
# Ángulos en radianes
print("Seno de pi/2:", math.sin(math.pi / 2))  # Muestra 1.0
print("Coseno de 0:", math.cos(0))            # Muestra 1.0
print("=====================================================")
print("e) Redondeos y manipulacion de numeros")
print("Redondeo hacia abajo de 2.9:", math.floor(2.9))  # Muestra 2
print("Redondeo hacia arriba de 2.1:", math.ceil(2.1))  # Muestra 3

